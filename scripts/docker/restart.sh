#!/bin/bash
# ============================================================================
# Docker 컨테이너 재시작 스크립트
# ============================================================================

set -e

SERVICE=$1

if [ -z "$SERVICE" ]; then
    echo "=========================================="
    echo "전체 서비스 재시작"
    echo "=========================================="
    docker-compose restart
    echo ""
    echo "✅ 모든 서비스가 재시작되었습니다."
else
    echo "=========================================="
    echo "$SERVICE 재시작"
    echo "=========================================="
    docker-compose restart $SERVICE
    echo ""
    echo "✅ $SERVICE가 재시작되었습니다."
fi

echo ""
docker-compose ps
echo ""

#!/bin/bash
# ============================================================================
# Docker 로그 확인 스크립트
# ============================================================================

# 서비스 이름 (인자가 있으면 해당 서비스만, 없으면 전체)
SERVICE=$1

if [ -z "$SERVICE" ]; then
    echo "=========================================="
    echo "전체 서비스 로그 (실시간)"
    echo "=========================================="
    echo "종료: Ctrl+C"
    echo ""
    docker-compose logs -f
else
    echo "=========================================="
    echo "$SERVICE 로그 (실시간)"
    echo "=========================================="
    echo "종료: Ctrl+C"
    echo ""
    docker-compose logs -f $SERVICE
fi
